package com.course.structure;
public class Building{
	private float sqFoot;
	private int stories;

	public void setSqFoot(float sqFoot){
		this.sqFoot = sqFoot;
	}

	public void setStories(int s){
		stories = s;
	}

	public float getSqFoot(){
		return sqFoot;
	}

	public float getStories(){
		return stories;
	}
}

 	